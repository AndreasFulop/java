public class Person {
    private String Hair = "Brown";
    private int Teeth = 32;
    private int Mouth = 1;

    public String getHair() {
        return Hair;
    }

    public void setHair(String hair) {
        Hair = hair;
    }

    public int getTeeth() {
        return Teeth;
    }

    public void setTeeth(int teeth) {
        Teeth = teeth;
    }

    public int getMouth() {
        return Mouth;
    }



}
