public class Main2_4 {
    public static void main(String[] args) {
        int emotion = 4;
        switch(emotion) {
            case 1: 
                System.out.println("You're happy");
                break;
            case 2:
                System.out.println("You're sad");
                break;
            case 3:
                System.out.println("You're angry");
                break;
            case 4:
                System.out.println("You're surprised");
                break;
 
            default:
                System.out.println("Unknown emotion");
            
            
        }
    }
}
